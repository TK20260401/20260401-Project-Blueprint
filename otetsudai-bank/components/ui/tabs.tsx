        // rpg variant: gold gradient active + pixel border
        "group-data-[variant=rpg]/tabs-list:h-9 group-data-[variant=rpg]/tabs-list:rounded-lg group-data-[variant=rpg]/tabs-list:px-3 group-data-[variant=rpg]/tabs-list:text-card-foreground group-data-[variant=rpg]/tabs-list:font-bold",
        "group-data-[variant=rpg]/tabs-list:data-active:bg-gradient-to-b group-data-[variant=rpg]/tabs-list:data-active:from-[#FFE066] group-data-[variant=rpg]/tabs-list:data-active:via-[#FFA623] group-data-[variant=rpg]/tabs-list:data-active:to-[#A86500]",
